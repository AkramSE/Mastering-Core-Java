/**
 * Demonstration of Variables, Data Types, and Literals in Java.
 * Understanding primitive data types and how literals are assigned.
 */
public class VariablesAndLiterals {
    public static void main(String[] args) {
        // Basic Variables & Addition
        int num1 = 4;
        int num2 = 4;
        int sum = num1 + num2;
        System.out.println("Basic Sum: " + sum);

        // Java Literals Demonstration
        byte age = 34; 
        long ageDino = 56666666666L; 
        char ch = 'A'; 
        float f = 5.6f; 
        double d = 5.66D; 
        boolean isJavaFun = true; 

        System.out.println("Byte value: " + age);
        System.out.println("Float literal: " + f);
        System.out.println("Char literal: " + ch);
    }
}
